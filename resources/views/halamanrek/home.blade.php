<h2>UTM</h2>

<h2>Teknik Informatika</h2>
<p>Kehidupan mahasiswa Teknik Informatika tentunya tidak akan jauh dari laptop ataupun komputer. 
Hal tersebut tentunya didukung oleh faktor mata kuliah mereka yang banyak berkaitan dengan laptop ataupun komputer. 
Dimulai dari mata kuliah, tugas dan praktikum, mereka akan banyak bergelut dnegan laptop. 
Mahasiswa jurusan Teknik Informatika akan terus bersama laptopnya atau komputernya, 
alat tempurnya untuk mengerjakan tugas adalah laptop. Oleh karena itu keseharian mereka tidak akan jauh dari laptop, 
tas-tas yang dulu saat SMA biasanya berisi buku, maka saat kuliah mereka akan lebih sering membawa laptop ke kampus.</p>
<h2>Alasan</h2>
<p>Saya mahasiswi Teknik Informatika Fakultas Ilmu Komputer Universitas Brawijaya. Alasan saya memilih prodi T
    eknik Informatika adalah karena ingin mendalami tentang dunia teknologi yang sudah menjadi kebutuhan semua 
    kalangan pada zaman sekarang. Saya juga ingin menjadi programmer yang bisa menguasai berbagai bahasa pemrograman
     dan menguasai keterampilan informatika dengan menganalisa dan membuat software sendiri. Selain itu alasan saya
     memilih prodi Teknik Informatika karena akan terbiasa terlatih untuk berpikir secara logis dan sistematis 
     untuk dapat dengan mudah menyesuaikan diri dengan pekerjaan apapun. Dan juga lulusan dari Teknik Informatika 
     sangat diminati oleh perusahaan-perusahaan di Indonesia bahkan perusahan dunia, lulusan Teknik Informatika juga 
     tidak hanya menjadi seorang pekerja tapi bisa membuat pekerjaan sendiri dan Teknik Informatika ini menjadi 
     jurusan yang paling diminati untuk itulah alasan saya memilih jurusan Teknik Informatika.</p>
